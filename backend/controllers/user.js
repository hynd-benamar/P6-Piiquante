//on install avec npm install --save bcrypt puis on importe le package bcrypt
const bcrypt = require('bcrypt');
//on install avec npm install --save jsonwebtoken puis on importe le package jsonwebtoken
const jwt = require('jsonwebtoken');
//pour l'authentification on a besoin d'importer notre model user
const User = require('../models/user')
//middleware d'authentifications
//creation de nouveaux users ds la base de données
exports.signup = (req, res, next) => {
    bcrypt.hash(req.body.password, 10)
        .then(hash => {
            const user = new User({
                email: req.body.email,
                password: hash
            });
            user.save()
                .then(() => res.status(201).json({ message: 'Utilisateur créé !' }))
                .catch(error => res.status(400).json({ error }));
        })
        .catch(error => res.status(500).json({ error }));
};
//verification des users déjà présent dans la base de données
exports.login = (req, res, next) => {
    console.log(req.body)
    User.findOne({ email: req.body.email })
        .then(user => {
            if (!user) {
                return res.status(401).json({ error: 'Utilisateur non trouvé !' });
            }
            bcrypt.compare(req.body.password, user.password)
                .then(valid => {
                    if (!valid) {
                        return res.status(401).json({ error: 'Mot de passe incorrect !' });
                    }
                    res.status(200).json({
                        userId: user._id,
                        token: jwt.sign(
                            { userId: user._id },
                            'RANDOM_TOKEN_SECRET',
                            { expiresIn: '24h' }
                        )
                    });
                })
                .catch(error => res.status(500).json({ error }));
        })
        .catch(error => res.status(500).json({ error }));
};